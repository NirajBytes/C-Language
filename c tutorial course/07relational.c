/*
                RELATIONAL OPERATORS

        operator            discription
        ==                  is equal to
        !=                  is not equal to     
        >                   greater than
        <                   less than
        >=                  greater than or equal to
        <=                  less than or equal to*/
        #include<stdio.h>
int main (){
    int a,b;
    a=34;
    b=5;
    printf("a==b = %d \n", a==b);
    printf("a!=b = %d \n", a!=b);
    printf("a>=b = %d \n",a>=b); 
    printf("a<=b = %d \n",a<=b);
    
    return 0;
}